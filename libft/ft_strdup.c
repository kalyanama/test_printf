#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	size_t	len;

	len = ft_strlen(src);
	dest = (char*)malloc(len + 1);
	if (dest)
		ft_strcpy(dest, src);
	return (dest);
}
